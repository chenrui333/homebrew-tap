    (testpath/"hello.py").write <<~PYTHON
      def greet(name):
          print(f"Hello, {name}!")

      greet("Homebrew")
    PYTHON

    output_log = testpath/"output.log"

    pid = spawn bin/"dhv", "hello.py", [:out, :err] => output_log.to_s
    sleep 1
    assert_match "RETURN_VALUE", output_log.read
  ensure
    Process.kill("TERM", pid)
    Process.wait(pid)
