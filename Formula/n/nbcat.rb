          }
        ],
        "metadata": {},
        "nbformat": 4,
        "nbformat_minor": 5
      }
    JSON

    assert_match version.to_s, shell_output("#{bin}/nbcat --version")

    output = shell_output("#{bin}/nbcat #{testpath/"sample.ipynb"}")
    assert_match "Sample Notebook", output
    assert_match "Notebook preview works.", output
    assert_match "hello from nbcat", output
