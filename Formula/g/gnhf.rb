  desc "Autonomous agent orchestrator for long-running coding tasks"
  homepage "https://github.com/kunchenguid/gnhf"
  url "https://registry.npmjs.org/gnhf/-/gnhf-0.1.24.tgz"
  sha256 "8826ab4611af84857b66cd497326a5cb8c3a8ec9313f056f8868600f9fb8357b"
  license "MIT"

  bottle do
    root_url "https://ghcr.io/v2/chenrui333/tap"
    sha256 cellar: :any_skip_relocation, all: "1b8b53022aad1aa180e258dcc010157a6ace936bd4ebf3116789405b4a44d80c"
  end

  depends_on "node"

