  homepage "https://github.com/kunchenguid/gnhf"
  url "https://registry.npmjs.org/gnhf/-/gnhf-0.1.37.tgz"
  sha256 "72d58c949c86ccb455776bf3fdfe875b1dc5ccc691d46fc6d6014960332211d0"
  license "MIT"

  bottle do
    root_url "https://ghcr.io/v2/chenrui333/tap"
    sha256 cellar: :any_skip_relocation, all: "54eea4a6111e0f33371552a8450ee8f9499de906e2fb117bcf129e6383d31eb8"
  end

