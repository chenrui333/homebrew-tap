  def install
    build_resources = %w[
      packaging
      pathspec
      pluggy
      trove-classifiers
      hatchling
      hatch-requirements-txt
    ]

    venv = virtualenv_create(libexec, "python3.13")
    venv.pip_install build_resources.map { |r| resource(r) }
    venv.pip_install resources.reject { |r| build_resources.include?(r.name) }
    venv.pip_install_and_link buildpath, build_isolation: false
  end

  test do
    assert_match version.to_s, shell_output("#{bin}/offpunk --version")
    assert_match ".pem", shell_output("#{libexec}/bin/python -c 'import certifi; print(certifi.where())'")
    output = pipe_output("#{bin}/ansicat --format plaintext", "# Heading\n\n=> gemini://example.org Example\n")
    assert_match "Heading", output
    assert_match "gemini://example.org Example", output
  end
